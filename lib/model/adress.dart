class Adress {
  final String adressId;
  final String nickname;
  final String complement;
  final String street;
  final String city;
  final String state;
  final String zipCode;

  Adress({
    required this.adressId,
    required this.nickname,
    required this.complement,
    required this.street,
    required this.city,
    required this.state,
    required this.zipCode,
  });
}
